import { chromium } from 'playwright'
const browser = await chromium.launch()
const page = await (await browser.newContext({ viewport: { width: 1500, height: 950 } })).newPage()
await page.goto('http://localhost:5174'); await page.waitForTimeout(900)
await page.locator('aside button').last().click(); await page.waitForTimeout(250)
await page.getByRole('button', { name: /Sara Iyer/ }).first().click(); await page.waitForTimeout(600)
await page.goto('http://localhost:5174/#/rules'); await page.waitForTimeout(500)
await page.getByRole('button', { name: /New rule/ }).click(); await page.waitForTimeout(500)
// EXACT suite sequence: trigger kind first
await page.locator('.animate-slide-in').getByRole('button', { name: 'It fires on a trigger' }).click()
await page.waitForTimeout(300)
const selects = page.locator('.animate-slide-in select')
console.log('SELECT COUNT after trigger:', await selects.count())
const opts0 = await selects.first().locator('option').allInnerTexts()
console.log('select[0] options:', opts0.slice(0, 6).join(' | '))
await selects.first().selectOption('Documents')
await page.waitForTimeout(500)
console.log('value:', await selects.first().inputValue())
const t = await page.locator('.animate-slide-in').last().innerText()
console.log('HAS Heads up:', t.includes('Heads up'))
console.log('HAS GDP:', t.includes('Global data protection'))
await browser.close()
